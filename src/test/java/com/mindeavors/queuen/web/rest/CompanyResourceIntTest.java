    @Test
    @Transactional
    public void updateCompany() throws Exception {
        // Initialize the database
        companyService.save(company);

        int databaseSizeBeforeUpdate = companyRepository.findAll().size();

        // Update the company
        Company updatedCompany = companyRepository.findOne(company.getId());
        updatedCompany
            .name(UPDATED_NAME)
            .address(UPDATED_ADDRESS)
            .logoUrl(UPDATED_LOGO_URL)
            .active(UPDATED_ACTIVE)
            .createdAt(UPDATED_CREATED_AT)
            .createdBy(UPDATED_CREATED_BY)
            .lastDeactivationBySuperAdmin(UPDATED_LAST_DEACTIVATION_BY_SUPER_ADMIN);

        restCompanyMockMvc.perform(put("/api/companies")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedCompany)))
            .andExpect(status().isOk());

        // Validate the Company in the database
        List<Company> companyList = companyRepository.findAll();
        assertThat(companyList).hasSize(databaseSizeBeforeUpdate);
        Company testCompany = companyList.get(companyList.size() - 1);
        assertThat(testCompany.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testCompany.getAddress()).isEqualTo(UPDATED_ADDRESS);
        assertThat(testCompany.getLogoUrl()).isEqualTo(UPDATED_LOGO_URL);
        assertThat(testCompany.isActive()).isEqualTo(UPDATED_ACTIVE);
        assertThat(testCompany.getCreatedAt()).isEqualTo(UPDATED_CREATED_AT);
        assertThat(testCompany.getCreatedBy()).isEqualTo(UPDATED_CREATED_BY);
        assertThat(testCompany.isLastDeactivationBySuperAdmin()).isEqualTo(UPDATED_LAST_DEACTIVATION_BY_SUPER_ADMIN);
    }

